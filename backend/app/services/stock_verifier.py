"""Stock ticker verification service.

Verifies LLM-generated stock tickers against authoritative stock lists,
falling back to Baidu search + LLM for unknown tickers.

Stock lists:
- A-shares: loaded from CSV (code, name)
- US stocks: loaded from CSV (code, name) — generated by scripts/fetch_stock_lists.py
- HK stocks: loaded from CSV (code, name) — generated by scripts/fetch_stock_lists.py

Verification flow:
1. Parse ticker string (e.g. "贵州茅台(600519.SH)" → name="贵州茅台", code="600519.SH")
2. Search local stock lists by code and name (fuzzy)
3. If found → return corrected name + code
4. If not found → Baidu search "{name} 股票代码" → LLM parse result
5. If Baidu confirms → return verified name + code
6. If Baidu cannot confirm → drop the ticker
"""
from __future__ import annotations

import asyncio
import csv
import json
import logging
import os
import re
from pathlib import Path
from typing import Any

import httpx
from openai import AsyncOpenAI

logger = logging.getLogger(__name__)


def _normalize_name(name: str) -> str:
    """Normalize a stock name: Traditional→Simplified Chinese, full-width→half-width, clean suffixes."""
    try:
        import zhconv
        name = zhconv.convert(name, "zh-cn")
    except ImportError:
        pass
    # Full-width → half-width for common chars (e.g. "－Ｗ" → "-W")
    name = name.translate(str.maketrans(
        "０１２３４５６７８９ＡＢＣＤＥＦＧＨＩＪＫＬＭＮＯＰＱＲＳＴＵＶＷＸＹＺ－",
        "0123456789ABCDEFGHIJKLMNOPQRSTUVWXYZ-",
    ))
    # Clean common HK suffixes: "-W", "-SW", "-SWR", "-R" (dual-class/weighted voting marks)
    name = re.sub(r"[-](?:SWR|SW|WR|W|R|S)$", "", name).strip()
    return name


def _format_ticker(name: str, code: str) -> str:
    """Format a verified ticker as 'name(code)', handling names that contain parentheses."""
    name = _normalize_name(name)
    # Strip inner parenthesized qualifiers from name (e.g. "西门子(ADR)" → "西门子")
    # to avoid confusing double-parentheses like "西门子(ADR)(SIEGY)"
    name = re.sub(r"\s*\([^)]*\)\s*$", "", name).strip()
    return f"{name}({code})"


def _extract_json(text: str) -> dict | None:
    """Best-effort JSON extraction from LLM output (handles <think> tags, markdown)."""
    text = re.sub(r"<think>.*?</think>", "", text, flags=re.DOTALL).strip()
    text = re.sub(r"<think>.*", "", text, flags=re.DOTALL).strip()
    try:
        return json.loads(text)
    except json.JSONDecodeError:
        pass
    m = re.search(r"```(?:json)?\s*(\{.*?\})\s*```", text, re.DOTALL)
    if m:
        try:
            return json.loads(m.group(1))
        except json.JSONDecodeError:
            pass
    depth = 0
    start = -1
    for i, ch in enumerate(text):
        if ch == '{':
            if depth == 0:
                start = i
            depth += 1
        elif ch == '}':
            depth -= 1
            if depth == 0 and start >= 0:
                try:
                    return json.loads(text[start:i + 1])
                except json.JSONDecodeError:
                    start = -1
    return None

