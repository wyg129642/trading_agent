"""Ingest MongoDB documents into Milvus as dense + sparse chunks.

Pipeline per doc:
    1. Load from Mongo (projection limited to spec.text_fields + metadata)
    2. Chunk (Markdown-aware, atomic-regex-guarded, recursive char splitter)
    3. Embed each chunk via TEI (batched 32–64 per HTTP round-trip)
    4. Upsert to Milvus with deterministic chunk_id → idempotent re-ingest

Design notes
------------
• Reuses ``kb_service.CollectionSpec`` / ``SPECS_LIST`` / ``_extract_*`` so the
  Mongo → canonical transformation is single-source-of-truth.
• Chunk_id is sha256(doc_id || chunk_index || chunk_hash)[:24] — stable across
  re-embeds, safe for Milvus PK.
• BM25 sparse_vector is auto-generated by Milvus Function (see
  ``init_kb_chunks.py``) — we do NOT ship sparse vectors from the client.
• LangChain MarkdownHeaderTextSplitter first, then RecursiveCharacterTextSplitter
  inside each section.
• Atomic regex guards stop the splitter from breaking tickers / quarters / %.
"""
